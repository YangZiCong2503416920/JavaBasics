public class Task1_Variables {
    static void main(String[] args) {
        //声明 int、double、String 类型变量，做加减乘除
        int a = 5;
        int b = 2;
        System.out.println(a + b);

        double d = 5.6;
        double f = 2.7;
        System.out.println(d - f);

        String s1 = "Hello";
        String s2 = "Java";
        System.out.println(s1 + s2);
    }
}
